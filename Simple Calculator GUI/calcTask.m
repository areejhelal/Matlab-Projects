 Y =get(handles.edit4,'string');
 Xnew = str2double(X);
 Ynew = str2double(Y);
 a = get(handles.popupmenu1,'value');
 switch(a)
    case 2
        Z = Xnew+Ynew;
    case 3
        Z = Xnew-Ynew;
    case 4
        Z = Xnew*Ynew;
    case 5
        Z = Xnew/Ynew;        
 end  
 set(handles.text5,'string',num2str(Z));
 b = get(handles.text5,'string');
 bnew = str2double(b);
 if bnew > 100.00
     imshow('sad.png')
 elseif bnew < 100.00 
     imshow('smile.jpg')
 elseif bnew == 100
     imshow('thinking.jpg')    
 end
 
